int		ft_iterative_factorial(int nb)
{
	int i;

	i = nb;
	if (nb == 1 || nb == 0)
		return (1);
	if (nb > 12 || nb < 0)
		return (0);
	else
	{
		while (i-- > 1)
			nb = nb * i;
	}
	return (nb);
}

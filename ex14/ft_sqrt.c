#include <stdio.h>

int		ft_sqrt(int nb)
{
	int		c;

	c = 0;
	while (nb != c * c)
	{
		if ((nb < c * c) || (nb <= 0))
		{
			return (0);
		}
		else
			c++;
	}
	return (c);
}
